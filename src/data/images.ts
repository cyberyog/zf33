export const getFeaturedImages = () => {
  return images.filter(image => image.featured);
};

export const getImagesByCategory = (category: string) => {
  return images.filter(image => image.category === category);
};

export const getImageById = (id: string) => {
  return images.find(image => image.id === id);
};

export const getRelatedImages = (imageId: string) => {
  const image = getImageById(imageId);
  if (!image || !image.related) return [];
  
  return image.related.map(id => getImageById(id)).filter(Boolean);
};